caixa_altura = float(input("qual e a altura da caixa? "))
largura_caixa = float(input("qual e a largura da caixa? "))
comprimento_caixa = float(input("qual e o comprimento da caixa? "))

volume_da_caixa = (caixa_altura * largura_caixa * comprimento_caixa)

print (f"o volume da caixa e {volume_da_caixa:.2f}")